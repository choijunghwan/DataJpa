//        assertThat(page.getTotalPages()).isEqualTo(2);   //전체 페이지 갯수
        assertThat(page.isFirst()).isTrue();           //첫 페이지 존재여부
        assertThat(page.hasNext()).isTrue();           //다음 거가 있는지 존재여부
